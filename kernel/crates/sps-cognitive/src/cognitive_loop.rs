//! SPS Cognitive Loop — The Single Mind (Wired).
//!
//! This is the integration layer that wires ALL SPS modules into one
//! unified cognitive pipeline. Every cognitive tool (PredictivePlanner,
//! ScenarioSimulator, DecisionScorer, MemoryConsolidator, ForgettingPolicy,
//! SelfImprovementLoop, CounterfactualEngine, AutonomousScheduler) is
//! actually CALLED at the appropriate step — not just dispatched as events.
//!
//! Pipeline:
//!   Input → Memory Recall → Reasoning → Decision Scoring → Goal Assessment
//!     → Predictive Planning → Scenario Simulation → Execution
//!     → Factory → Reflection → Counterfactual Analysis
//!     → Memory Store + Consolidation + Forgetting
//!     → Self-Improvement → Output

